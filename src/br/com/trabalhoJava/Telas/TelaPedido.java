    private void btnPedAddSomaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPedAddSomaActionPerformed
        // TODO add your handling code here:
        if (tblPedido.getSelectedRow() != -1) {
            NumberFormat formatador = new DecimalFormat("#0.00");
            valor += Float.parseFloat(lblValorPedido.getText());
            lblPedidoTotal.setText(String.valueOf(formatador.format(valor)));
            
            int setar = tblPedido.getSelectedRow();
            
            if(lblValorPedido.getText() != "0"){
            DefaultTableModel modelo = (DefaultTableModel) tblPedidoRemove.getModel();
            modelo.addRow(new String[]{lblNomePedido.getText(), lblValorPedido.getText()});
            linhas++;}
        }else {
            //Aviso para setar um produto
            JOptionPane.showMessageDialog(null, "Selecione um produto para Adicionar!");
        }

    }//GEN-LAST:event_btnPedAddSomaActionPerformed

    private void btnPedAddSubtraiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPedAddSubtraiActionPerformed
        // TODO add your handling code here:
        if (tblPedidoRemove.getSelectedRow() != -1) {
        NumberFormat formatador = new DecimalFormat("#0.00");
        valor -= Float.parseFloat(lblValorPedido.getText());
        ((DefaultTableModel) tblPedidoRemove.getModel()).removeRow(tblPedidoRemove.getSelectedRow());
        lblPedidoTotal.setText(String.valueOf(formatador.format(valor)));
        linhas= linhas - 1;
        }else {
            //Aviso para setar um produto
            JOptionPane.showMessageDialog(null, "Selecione um produto para Remover!");
        }

    }//GEN-LAST:event_btnPedAddSubtraiActionPerformed

