    public void lerTabel() throws SQLException {
        //criando um padrão de jtable
        DefaultTableModel modelo = (DefaultTableModel) tblPedido.getModel();
        modelo.setNumRows(0);
        ProdutoDao pdao = new ProdutoDao();
        //laço de repeição para ler as linhas da tabela
        for (Produtos p : pdao.ler()) {
            modelo.addRow(new Object[]{
                p.getCategoria(),
                p.getNome(),
                p.getPreco()

            });
        }
    }

    public void lerTabelPesq(String pesq) throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tblPedido.getModel();
        modelo.setNumRows(0);
        ProdutoDao pdao = new ProdutoDao();
        for (Produtos p : pdao.lerpesquisa(pesq)) {
            modelo.addRow(new Object[]{
                p.getCategoria(),
                p.getNome(),
                p.getPreco()

            });
        }

    }

  

